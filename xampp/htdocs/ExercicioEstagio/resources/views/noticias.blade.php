The Witcher 3: Wild Hunt (Polish: Wiedźmin 3: Dziki Gon) is an action role-playing video game developed by CD Projekt and published by CD Projekt RED. It was released worldwide on 19 May 2015 for Microsoft Windows, PlayStation 4, and Xbox One. The game is the third in the series, preceded by The Witcher and The Witcher 2: Assassins of Kings, which are based on the series of fantasy novels by Polish author Andrzej Sapkowski.

Played in an open world third-person perspective, players control protagonist Geralt of Rivia, a monster hunter known as a witcher, who seeks to find his adopted daughter on the run from the "Wild Hunt", an otherworldly force determined to capture and utilize her powers. Throughout the game, players battle against the world's many dangers using weapons and magic, interact with various non-player characters, and complete main story quests, side quests, bounty contracts, and treasure hunts to acquire experience points and gold used to increase Geralt's various abilities and equipment. The game's central story features multiple endings that are determined by Geralt's choices made by the player during certain points of the story.

The Witcher 3: Wild Hunt was met with widespread critical acclaim upon its release, with praise directed toward its gameplay, narrative, world design, combat and visuals. It became the most awarded game of 2015, receiving numerous Game of the Year awards from various gaming publications, critics, and award events, and is regarded by many to be one of the greatest role-playing games of all time. The game was also a commercial success, selling over six million copies within six weeks of its release. Two expansion packs for the game were also released, Hearts of Stoneand Blood and Wine, with both receiving further critical acclaim. A Game of the Year edition, which includes the base game, its two expansion packs, and all additional downloadable content, was released in August 2016.

It even beat Bethesda's Fallout 4 and bagged GOTY award.

